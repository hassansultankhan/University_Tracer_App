                    child: Text(countrylist[index],
                        style: CustomTextStyle.stylepointer),
                  ),
                  onTap: () => loadCountry(countrylist[index]),
                  ),
                ],
              )),
        ),
      ),
    );
  }
  
    loadCountry(String country){
      Navigator.push(context, MaterialPageRoute(builder:(context) => homescreen(country),));
    }
